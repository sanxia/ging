type CustomError struct {
	Code int32
	Msg  string
}

/* ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
 * 实例化自定义错误
 * ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++ */
func NewCustomError(msg string) *CustomError {
	return &CustomError{
		Code: 111,
		Msg:  msg,
	}
}

/* ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
 * 实例化自定义错误
 * ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++ */
func NewError(code int32, msg string) *CustomError {
	return &CustomError{
		Code: code,
		Msg:  msg,
	}
}

/* ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
 * error接口实现
 * ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++ */
func (err CustomError) Error() string {
	return err.Msg
}
